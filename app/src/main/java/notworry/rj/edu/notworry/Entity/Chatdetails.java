package notworry.rj.edu.notworry.Entity;

public class Chatdetails {
	private int chatdetails_id;
	private int chatgroup_id;
	private int user_id;
	private String chat_things;
	private String chat_time;
	public int getChatdetails_id() {
		return chatdetails_id;
	}
	public void setChatdetails_id(int chatdetails_id) {
		this.chatdetails_id = chatdetails_id;
	}
	public int getChatgroup_id() {
		return chatgroup_id;
	}
	public void setChatgroup_id(int chatgroup_id) {
		this.chatgroup_id = chatgroup_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getChat_things() {
		return chat_things;
	}
	public void setChat_things(String chat_things) {
		this.chat_things = chat_things;
	}
	public String getChat_time() {
		return chat_time;
	}
	public void setChat_time(String chat_time) {
		this.chat_time = chat_time;
	}
	
    
}
